package org.example;

public class Ifandelse {
    public static void main(String[] args) {
        int value = 30;
        if(value > 35){
            System.out.println("Pass");
        } else if (value > 80) {
            System.out.println("Good Mark");

        }
        else {
            System.out.println("fail");
        }

    }
}
